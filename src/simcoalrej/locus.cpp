#include "locus.h"

//------------------------------------------------------------------------------
TLocus::TLocus(const TLocus& L) : data(0, 10) {
   sampSize=L.sampSize;
   data.Reallocate(sampSize);
   for (int i=0; i<sampSize; ++i) {
      data.Add(L.data[i]);
   }
}                    
